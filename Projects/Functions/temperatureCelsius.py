def thermometer(fahrenheit):
    celsius = (fahrenheit - 32) * 5 / 9
    return(celsius)


print("patient no. 1 temperature in celsius is = ",thermometer(101))
print("patient no. 2 temperature in celsius is = ",thermometer(96))
print("patient no. 3 temperature in celsius is = ",thermometer(120))
